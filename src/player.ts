class Player{
    fname : string;
    lname : string;
    jerseyNum : string;
    position: string;
    hasBirthDate: boolean;

    constructor(fname: string, lname: string, jerseyNum: string, position: string, hasBirthDate: boolean){
        this.fname=fname
        this.lname=lname
        this.jerseyNum=jerseyNum
        this.position=position
        this.hasBirthDate= hasBirthDate
    }
}